export const statusSelect = (status, type) => {
  if (type == "1") {
    switch (status) {
      case "1":
        return "本人";
      case "2":
        return "律师";
      case "3":
        return "监护人";
      case "4":
        return "其他代理人";
      default:
        break;
    }
  } else if (type == "2") {
    switch (status) {
      case "1":
        return "自然人";
      case "2":
        return "法人";
      case "3":
        return "非法人组织";
      default:
        break;
    }
  } else if (type == "3") {
    switch (status) {
      case "1":
        return "个人";
      case "2":
        return "法人";
      case "3":
        return "非法人组织";
      default:
        break;
    }
  } else if (type == "4") {
    switch (status) {
      case "1":
        return "律师";
      case "2":
        return "监护人";
      case "3":
        return "亲友";
      case "4":
        return "其他";
      default:
        break;
    }
  }
};
